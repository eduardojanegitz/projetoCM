    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        
        try {
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data_venda = LocalDate.parse(txtData.getText(), formato);
            
            double total_venda;
            VendasDAO vendasDAO = new VendasDAO();
            total_venda = vendasDAO.retornaTotalVendaPorData(data_venda);
            
            txtTotalVenda.setText(String.valueOf(total_venda));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Informe a data corretamente. " + e);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

